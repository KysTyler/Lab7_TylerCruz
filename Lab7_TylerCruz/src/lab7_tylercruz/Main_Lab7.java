            DefaultComboBoxModel modelo2 = (DefaultComboBoxModel) CB_CarsToEmployee.getModel();
            modelo2.addElement(x);
            CB_CarsToEmployee.setModel(modelo2);
            //Separator
            bitacora2 ap = new bitacora2("./Empleado.cbm");
            ap.cargarArchivo();
